import keras

#-------------------------------------------------------------------------------
#
#-------------------------------------------------------------------------------
class KerasTokenizer() :
    '''Encapsulation of keras tokenizer class.
    This is a container allowing to pipe tokenization process.
    '''
    #---------------------------------------------------------------------------
    #
    #---------------------------------------------------------------------------
    def __init__(self) :
        self.tokenizer = keras.preprocessing.text.Tokenizer()
    #---------------------------------------------------------------------------
    
    #---------------------------------------------------------------------------
    #
    #---------------------------------------------------------------------------
    def fit(self,X):
        pass
    #---------------------------------------------------------------------------
    
    #---------------------------------------------------------------------------
    #
    #---------------------------------------------------------------------------
    def fit_on_texts(self,X):
        self.tokenizer.fit_on_texts(X)
    #---------------------------------------------------------------------------
    
    #---------------------------------------------------------------------------
    #
    #---------------------------------------------------------------------------
    def texts_to_sequences(self, X):
        return self.tokenizer.texts_to_sequences(X)
    #---------------------------------------------------------------------------
    
    #---------------------------------------------------------------------------
    #
    #---------------------------------------------------------------------------
    def transform(self,X):
        return self
    #---------------------------------------------------------------------------        
#-------------------------------------------------------------------------------    

